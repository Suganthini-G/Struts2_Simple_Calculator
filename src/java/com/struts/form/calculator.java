package com.struts.form;

/**
 *
 * @author Sugaa
 */
public class calculator {
    
    public float add(float n1, float n2){
        return n1+n2;
    }
    
    public float sub(float n1, float n2){
        return n1-n2;
    }
    
    public float mul(float n1, float n2){
        return n1*n2;
    }
    
    public float div(float n1, float n2){
        return n1/n2;
    }
    
}
